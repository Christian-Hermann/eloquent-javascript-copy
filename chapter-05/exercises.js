// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(value, test, update, body) {
for (let currentValue = value; test(currentValue); currentValue = update(currentValue)){
  body(currentValue);
} 
}

